}

if [ $# -eq 0 ]; then
  CMD=""
  SCRIPT_ARGS=""
else
  CMD="$1"
  case "$CMD" in
    --*)
      CMD=""
      SCRIPT_ARGS="$@"
      ;;
    *)
      shift
      SCRIPT_ARGS="$@"
      ;;
  esac
fi

case "$CMD" in
  "") all ;;
  all) all ;;
  run) run ;;
  run-kvm) run_kvm ;;
